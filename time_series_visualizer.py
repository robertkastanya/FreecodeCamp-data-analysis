import matplotlib.pyplot as plt
import pandas as pd
import seaborn as sns
from pandas.plotting import register_matplotlib_converters
register_matplotlib_converters()

# --- 1. Import and Clean Data ---
# Import data (Make sure to parse dates and set the index to the 'date' column)
df = pd.read_csv("fcc-forum-pageviews.csv", parse_dates=["date"], index_col="date")

# Clean data by filtering out top 2.5% and bottom 2.5% of page views
df = df[
    (df["value"] >= df["value"].quantile(0.025)) &
    (df["value"] <= df["value"].quantile(0.975))
]


# --- 2. Line Plot ---
def draw_line_plot():
    # Draw line plot
    fig, ax = plt.subplots(figsize=(15, 5))
    ax.plot(df.index, df["value"], color="red", linewidth=1)

    # Titles and labels
    ax.set_title("Daily freeCodeCamp Forum Page Views 5/2016-12/2019")
    ax.set_xlabel("Date")
    ax.set_ylabel("Page Views")

    # Save image and return fig (don't change this part)
    fig.savefig('line_plot.png')
    return fig


# --- 3. Bar Plot ---
def draw_bar_plot():
    # Copy and modify data for monthly bar plot
    df_bar = df.copy()
    df_bar["year"] = df_bar.index.year
    df_bar["month"] = df_bar.index.strftime("%B")

    # Group by year and month, calculate mean, and unstack
    df_grouped = df_bar.groupby(["year", "month"])["value"].mean().unstack()

    # Reorder columns to ensure chronological month order
    months_order = [
        "January", "February", "March", "April", "May", "June",
        "July", "August", "September", "October", "November", "December"
    ]
    df_grouped = df_grouped.reindex(columns=months_order)

    # Draw bar plot
    fig = df_grouped.plot(kind="bar", figsize=(12, 10), xlabel="Years", ylabel="Average Page Views").get_figure()
    plt.legend(title="Months", labels=months_order)

    # Save image and return fig (don't change this part)
    fig.savefig('bar_plot.png')
    return fig


# --- 4. Box Plot ---
def draw_box_plot():
    # Prepare data for box plots (this part is usually provided in the boilerplate)
    df_box = df.copy()
    df_box.reset_index(inplace=True)
    df_box['year'] = [d.year for d in df_box.date]
    df_box['month'] = [d.strftime('%b') for d in df_box.date]

    # Reorder months starting from Jan for the seasonality plot
    months_order = ["Jan", "Feb", "Mar", "Apr", "May", "Jun", "Jul", "Aug", "Sep", "Oct", "Nov", "Dec"]

    # Set up the matplotlib figure (1 row, 2 columns)
    fig, axes = plt.subplots(1, 2, figsize=(20, 8))

    # Draw Year-wise Box Plot (Trend)
    sns.boxplot(x="year", y="value", data=df_box, ax=axes[0], flierprops={"marker": "d", "markersize": 2})
    axes[0].set_title("Year-wise Box Plot (Trend)")
    axes[0].set_xlabel("Year")
    axes[0].set_ylabel("Page Views")

    # Draw Month-wise Box Plot (Seasonality)
    sns.boxplot(x="month", y="value", data=df_box, order=months_order, ax=axes[1], flierprops={"marker": "d", "markersize": 2})
    axes[1].set_title("Month-wise Box Plot (Seasonality)")
    axes[1].set_xlabel("Month")
    axes[1].set_ylabel("Page Views")

    # Save image and return fig (don't change this part)
    fig.savefig('box_plot.png')
    return fig

