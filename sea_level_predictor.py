import pandas as pd
import matplotlib.pyplot as plt
from scipy.stats import linregress

def draw_plot():
    # 1. Use Pandas to import the data from epa-sea-level.csv
    df = pd.read_csv('epa-sea-level.csv')

    # 2. Use matplotlib to create a scatter plot
    # x-axis: Year, y-axis: CSIRO Adjusted Sea Level
    plt.figure(figsize=(10, 6))
    plt.scatter(df['Year'], df['CSIRO Adjusted Sea Level'], color='blue', label='Original Data', s=10)

    # 3. First line of best fit (using all data from 1880)
    # Get slope and y-intercept
    res_all = linregress(df['Year'], df['CSIRO Adjusted Sea Level'])

    # Create a new series of years from 1880 to 2050 for prediction
    years_all = pd.Series([i for i in range(1880, 2051)])
    # Calculate the predicted sea level: y = mx + c
    sea_levels_all = res_all.slope * years_all + res_all.intercept

    # Plot the first line of best fit
    plt.plot(years_all, sea_levels_all, 'r', label='Best Fit Line (1880-2050)')

    # 4. Second line of best fit (using data from year 2000 onwards)
    # Filter data for years >= 2000
    df_recent = df[df['Year'] >= 2000]

    # Get slope and y-intercept for the recent data
    res_recent = linregress(df_recent['Year'], df_recent['CSIRO Adjusted Sea Level'])

    # Create a new series of years from 2000 to 2050 for prediction
    years_recent = pd.Series([i for i in range(2000, 2051)])
    # Calculate the predicted sea level: y = mx + c
    sea_levels_recent = res_recent.slope * years_recent + res_recent.intercept

    # Plot the second line of best fit
    plt.plot(years_recent, sea_levels_recent, 'green', label='Best Fit Line (2000-2050)')

    # 5. Add labels, title, and legend
    plt.xlabel('Year')
    plt.ylabel('Sea Level (inches)')
    plt.title('Rise in Sea Level')
    plt.legend()

    # Save plot and return data for testing (DO NOT MODIFY THESE TWO LINES)
    plt.savefig('sea_level_plot.png')
    return plt.gca()

